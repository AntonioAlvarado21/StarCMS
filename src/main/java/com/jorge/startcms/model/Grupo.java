package com.jorge.startcms.model;

public class Grupo {

	private long IdGrupo;
	private String Nombre;
	
	//GETTER Y SETTERS
	public long getIdGrupo() {
		return IdGrupo;
	}
	public void setIdGrupo(long idGrupo) {
		IdGrupo = idGrupo;
	}
	public String getNombre() {
		return Nombre;
	}
	public void setNombre(String nombre) {
		Nombre = nombre;
	}
	
	
	
}
